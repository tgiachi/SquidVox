namespace SquidVox.Core.Notifications;

/// <summary>
/// Defines the set of predefined notification styles.
/// </summary>
public enum NotificationType
{
    /// <summary>
    /// Represents the default notification style.
    /// </summary>
    Default,

    /// <summary>
    /// Represents an informational notification style.
    /// </summary>
    Info,

    /// <summary>
    /// Represents a success notification style.
    /// </summary>
    Success,

    /// <summary>
    /// Represents a warning notification style.
    /// </summary>
    Warning,

    /// <summary>
    /// Represents an error notification style.
    /// </summary>
    Error
}
